def test_default_since_sha_uses_last_published(app_instance):
    """When since_sha is omitted, endpoint uses the last published release's commit_sha."""
    admin = _make_user(email="gen-admin-sha@gen-test.com", role=UserRole.admin)

    expected_sha = "zzz999_local_meta"
    db = _db_mod.SessionLocal()
    try:
        rel = Release(
            title="Gen SHA Boundary Release",
            body=None,
            summary="Boundary helper",
            highlights=[{"category": "fixed", "text": "Boundary"}],
            status=ReleaseStatus.published,
            created_by_id=admin.id,
            commit_sha_at_release=expected_sha,
            # Far-future to win the latest-published race in shared DB.
            published_at=datetime(2099, 6, 1, tzinfo=timezone.utc),
        )
        db.add(rel)
        db.commit()
    finally:
        db.close()

    tc = _make_authenticated_client(app_instance, admin)

    meta = _meta([_commit("fff666", "feat: default sha test")])
    suggestion = ReleaseNotesSuggestion(
        title="Default SHA Test",
        summary="A new feature",
        highlights=[{"category": "new", "text": "New feature"}],
        low_confidence=False,
    )
    mock_read = MagicMock(return_value=meta)

    with (
        patch(_MOCK_AI, return_value=True),
        patch(_MOCK_READ, mock_read),
        patch(_MOCK_GEN, return_value=suggestion),
    ):
        # Empty body → since_sha falls back to last published release's SHA.
        res = tc.post(_ENDPOINT, json={})

    assert res.status_code == 200
    # Cleanup: backdate so this release doesn't pollute downstream tests.
    db = _db_mod.SessionLocal()
    try:
        row = db.query(Release).filter_by(commit_sha_at_release=expected_sha).one()
        row.published_at = datetime(2020, 1, 1, tzinfo=timezone.utc)
        db.commit()
    finally:
        db.close()


