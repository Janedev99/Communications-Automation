"""Tests for POST /api/v1/admin/releases/draft-from-commits.

Endpoint shape (post chunk-2 refactor):
    Request:  {since_sha?: str}
    Response: DraftSuggestionResponse with structured highlights
    Error:    422 ai_unavailable | 422 release_meta_unavailable | 502 llm_error

Source-of-truth for commits is backend/release-meta.json (a build-time
snapshot generated by scripts/generate_release_meta.py). Tests mock the
reader at the SUT's import path: app.api.admin_releases.read_release_meta.
"""
